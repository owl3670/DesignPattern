package command.simpleremoteWL

class GarageDoor {
    fun up() {
        println("Garage Door is Up")
    }

    fun down() {
        println("Garage Door is Down")
    }

    fun stop() {
        println("Garage Door is Stopped")
    }

    fun lightOn() {
        println("Garage light is on")
    }

    fun lightOff() {
        println("Garage light is off")
    }
}